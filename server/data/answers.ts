const pools = [round1Pool, round2Pool, round3Pool];

/**
 * Draw `count` answer cards from the pool for a given round (1-based).
 * Returns unique cards per call using Fisher-Yates shuffle on a copy.
 */
export function drawAnswerCards(
  round: number,
  count: number = ANSWER_CARDS_PER_HAND
): AnswerOption[] {
  const pool = pools[round - 1] || pools[0];
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count).map((text, i) => ({
    id: `a-${round}-${i}-${Date.now().toString(36)}`,
    text,
  }));
}
